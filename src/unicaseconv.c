int convertLetter(int a, struct ccaseconv * T)
    {
    int i;
    if (a > 0x10FFFF)
        return a;
    for (i = 0;; ++i)
        {
        if ((unsigned int)a < T[i].L)
            {
            if (i == 0)
                return a;
            --i;
            if (  (unsigned int)a <= (unsigned int)(T[i].L + T[i].range)
               && (  T[i].inc < 2
                  || !((a - T[i].L) & 1)
                  )
               )
                {
                return a + T[i].dif;
                }
            else
                {
                break;
                }
            }
        }
    return a;
    }

